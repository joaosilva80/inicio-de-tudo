#Peça ao usuário dois números e exiba a soma, subtração, multiplicação, divisão e o resto da divisão entre eles.
#
um=int(input('Digite dois números'))
dois=int(input('Digite dois números'))
a=um+dois
s=um-dois
m=um*dois
d=um/dois
r=um%dois
print('A soma é:',a)
print('A subtração é:',s)
print('A multiplicação é:',m)
print('A divisão é:',d)
print('O resto é:',r)
